function erf(x) {
  const a1 = 0.254829592, a2 = -0.284496736, a3 = 1.421413741,
        a4 = -1.453152027, a5 = 1.061405429, p = 0.3275911;
  const s = x < 0 ? -1 : 1;
  x = Math.abs(x);
  const t = 1 / (1 + p * x);
  return s * (1 - (((((a5 * t + a4) * t) + a3) * t + a2) * t + a1) * t * Math.exp(-x * x));
}

export function normCDF(z) {
  return 0.5 * (1 + erf(z / Math.sqrt(2)));
}

function calcZScore(value, lms) {
  const [L, M, S] = lms;
  if (Math.abs(L) < 0.0001) return Math.log(value / M) / S;
  return (Math.pow(value / M, L) - 1) / (L * S);
}

export function calcPercentile(value, table, sex, months) {
  const idx = Math.min(Math.round(months), table[sex].length - 1);
  const lms = table[sex][idx];
  const z = calcZScore(value, lms);
  return {
    percentile: Math.round(normCDF(z) * 1000) / 10,
    z: Math.round(z * 100) / 100,
    median: lms[1],
  };
}

export function percentileInfo(p) {
  if (p < 3)  return { color: "#EF5350", bg: "#FFEBEE", label: "Bajo (<P3)", zone: "bajo" };
  if (p < 15) return { color: "#FF9800", bg: "#FFF3E0", label: "Bajo-normal (P3-P15)", zone: "vigilar" };
  if (p < 85) return { color: "#26C6A1", bg: "#E0FAF4", label: "Normal (P15-P85)", zone: "normal" };
  if (p < 97) return { color: "#FF9800", bg: "#FFF3E0", label: "Alto-normal (P85-P97)", zone: "vigilar" };
  return { color: "#EF5350", bg: "#FFEBEE", label: "Alto (>P97)", zone: "alto" };
}
